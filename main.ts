function Jumpcount1 () {
    if (controller.A.isPressed()) {
        if (Jumpcount == 2) {
            Player1.vy = 0
            Jumpcount = 2
        } else {
            Player1.vy = -120
            Jumpcount += 1
        }
    }
    if (Player1.isHittingTile(CollisionDirection.Bottom)) {
        Jumpcount = 0
    }
}
